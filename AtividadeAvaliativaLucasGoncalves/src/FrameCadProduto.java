    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        Categoria cat = (Categoria) cmbCategorias.getSelectedItem();        
        Produto prod = new Produto();
        
        prod.setNome(txtNomeProduto.getText());
        prod.setPreco(Double.parseDouble(txtPrecoProd.getText()));
        prod.setCategoria(cat.getId());
        
        prod.setCod_barras(Integer.parseInt(txtCodBarras.getText()));
        
        ProdutoDAO pDAO = new ProdutoDAO();
        pDAO.inserir(prod);
        JOptionPane.showMessageDialog(null, "Produto inserido com sucesso!");
        limparFormulario();
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        Categoria c = (Categoria) cmbCategorias.getSelectedItem();
        int id_produto = Integer.parseInt(txtID.getText());
        ProdutoDAO pDAO = new ProdutoDAO();

        Produto p = pDAO.getProduto(id_produto);
        if(p == null){
            txtID.setText("");
            JOptionPane.showMessageDialog(this, "Produto não encontrado!");
            //limparFormulario();
        }else{
            txtID.setText(txtID.getText());
            txtNomeProduto.setText(p.getNome());
            txtPrecoProd.setText(String.valueOf(p.getPreco()));

            cmbCategorias.setSelectedItem(c.getId());
            txtCodBarras.setText(String.valueOf(p.getCod_barras()));
            }//fim do 
        //Fim do consultar pessoa
    }//GEN-LAST:event_btnConsultarActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        try {
            int id = Integer.parseInt(txtID.getText());
            String nome = txtNomeProduto.getText();
            double preco = Double.parseDouble(txtPrecoProd.getText());
            int codBarras = Integer.parseInt(txtCodBarras.getText());
            
            Categoria cat = (Categoria) cmbCategorias.getSelectedItem();
            Produto prod = new Produto();
            
            prod.setId_produto(id);
            prod.setNome(nome);
            prod.setPreco(preco);
            prod.setCategoria(cat.getId());
            prod.setCod_barras(codBarras);

            ProdutoDAO pDAO = new ProdutoDAO();
            pDAO.editarProduto(prod);

            JOptionPane.showMessageDialog(this, "Produto alterado com sucesso!");

        }catch(Exception e) {
        JOptionPane.showMessageDialog(this, "Erro ao alterar produto: " + e.getMessage());
        }
        limparFormulario();
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        try {
            int id = Integer.parseInt(txtID.getText());

            int confirmacao = JOptionPane.showConfirmDialog(this,
                    "Tem certeza que deseja excluir este produto?",
                    "Confirmar exclusão",
                    JOptionPane.YES_NO_OPTION);

            if (confirmacao == JOptionPane.YES_OPTION) {
                ProdutoDAO pDAO = new ProdutoDAO();
                pDAO.excluirProduto(id);
                JOptionPane.showMessageDialog(this, "Produto excluído com sucesso!");
                limparFormulario();
            }

        } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erro ao excluir produto: " + e.getMessage());
        }
    }//GEN-LAST:event_btnExcluirActionPerformed


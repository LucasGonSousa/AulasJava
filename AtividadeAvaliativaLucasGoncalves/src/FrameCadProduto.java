    private void carregarCategorias() {
        CategoriaDAO catDAO = new CategoriaDAO();
        List<Categoria> categorias = catDAO.getCategoria();
        cmbCategorias.removeAllItems();
        for (Categoria cat : categorias){ 
            cmbCategorias.addItem(cat); 
        }
    }
    
